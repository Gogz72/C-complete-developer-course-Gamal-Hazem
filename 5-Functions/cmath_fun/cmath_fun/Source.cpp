#include <iostream>
#include <cmath>

using namespace std;

int main()
{
	float powresult = pow(2, 5);
	float sqrtresult = sqrt(512);
	int ceilresult = ceil(4.2);
	int floorresult = floor(4.2);
	float log2result = log2(512);

	cout << "powresult is = " << powresult << endl;
	cout << "sqrtresult is = " << sqrtresult << endl;
	cout << "ceilresult is = " << ceilresult << endl;
	cout << "floorresult is = " << floorresult << endl;
	cout << "log2result is = " << log2result << endl;



	return 0;
}